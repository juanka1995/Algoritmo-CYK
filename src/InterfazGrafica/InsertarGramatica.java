    public static void reiniciarJTable(javax.swing.JTable table){
        DefaultTableModel dm = (DefaultTableModel)table.getModel();
        dm.getDataVector().removeAllElements();
        dm.fireTableDataChanged();
    }
    
    private void mostrarGramaticaTabla(){
        reiniciarJTable(jTable1);
        Iterator it = miGramatica.getMapContent().keySet().iterator();
        while(it.hasNext()){
            String key = (String) it.next();
            String value = miGramatica.getMapContent().get(key).toString();
            dtm.addRow(new Object[]{value,key});
        }
    }

