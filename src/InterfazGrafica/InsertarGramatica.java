    private void bExplorarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExplorarActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Seleccionar");
        int result = chooser.showSaveDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File f = chooser.getSelectedFile();
            String pathFile = f.getAbsolutePath();

            jTFpath.setText(pathFile);

            try {
                miGramatica = new Gramatica(pathFile);
            } catch (IOException ex) {
                Logger.getLogger(InsertarGramatica.class.getName()).log(Level.SEVERE, null, ex);
            }
            mostrarGramaticaTabla();
        }
        
    }//GEN-LAST:event_bExplorarActionPerformed

    private void jBcontinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcontinuarActionPerformed
        if(jTFpath.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor, selecciona el fichero con la gramática deseada para poder continuar.", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            this.setVisible(false);
            algoritmoCYK = new AlgoritmoCYK(miGramatica, this);
            reiniciarJTable(jTable1);
            jTFpath.setText("");
            jTFpath.requestFocus();
            algoritmoCYK.setVisible(true);
        }
    }//GEN-LAST:event_jBcontinuarActionPerformed

